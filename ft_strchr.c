#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] && s && (char)c != s[i])
		i++;
	if (s[i] == c)
		return ((char *)&s[i]);
	return (0);
}
