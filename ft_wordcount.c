#include "libft.h"

int		ft_wordcount(char const *s, char c)
{
	int		i;

	i = 0;
	if (*s == 0)
		return (i);
	while (*s != '\0')
	{
		if (*s && *s != c && (*(s + 1) == c || *(s + 1) == '\0'))
			i++;
		s++;
	}
	while (*s != '\0')
		s--;
	return (i);
}
